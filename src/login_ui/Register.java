    private void btnCreateAccountMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCreateAccountMouseClicked
        // TODO add your handling code here:
        checkRepassword();
        checkUsername();
        checkEmpty();
        checkRepassword();
        if(checkID){
            JLabel label = new JLabel("มีผู้ใช้นี้เเล้ว");
            label.setFont(new Font("TH Sarabun New", Font.PLAIN, 15));
            JOptionPane.showMessageDialog(null, label,"Error",JOptionPane.WARNING_MESSAGE);
            reset();
        }else{
            if(checkEmpty()){
                JLabel label = new JLabel("กรุณากรอกข้อมูลให้ครบถ้วน");
                label.setFont(new Font("TH Sarabun New", Font.PLAIN, 15));
                JOptionPane.showMessageDialog(null,label,"ERROR",JOptionPane.WARNING_MESSAGE);
                reset();
            }else{
                if(!checkEmpty()){
                    if(!checkRepassword()){
                        JLabel label = new JLabel("รหัสผ่านไม่ตรงกัน");
                        label.setFont(new Font("TH Sarabun New", Font.PLAIN, 15));
                        JOptionPane.showMessageDialog(null,label,"ERROR",JOptionPane.WARNING_MESSAGE);
                        reset();
                    }else{
                        submitRegister();
                        reset();
                    }
                }
            }
        }
    }//GEN-LAST:event_btnCreateAccountMouseClicked

