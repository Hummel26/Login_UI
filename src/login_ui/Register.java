    public void checkUsername(){
        MongoClientURI uri = new MongoClientURI("mongodb://hummel26:hummel26@ds141942.mlab.com:41942/ox");
        MongoClient client = new MongoClient(uri);
        MongoDatabase db = client.getDatabase(uri.getDatabase());
        
        MongoCollection<Document> reg = db.getCollection("Register");
        
        try{
            Document myDoc = reg.find(eq("username", txtUsername.getText())).first();
            System.out.println(myDoc.toJson());
            System.out.println("มี username เเล้วในระบบ");
            checkID = true;
        }catch(Exception e){
            System.out.println("username ยังไม่มีในระบบ");
            checkID = false;
        }
    }
    
