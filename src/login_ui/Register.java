    public void submitRegister(){
        MongoClientURI uri = new MongoClientURI("mongodb://hummel26:hummel26@ds141942.mlab.com:41942/ox");
        MongoClient client = new MongoClient(uri);
        MongoDatabase db = client.getDatabase(uri.getDatabase());
        
        MongoCollection<Document> reg = db.getCollection("Register");
        
        List<Document> seedData = new ArrayList<Document>();       
        seedData.add(new Document("username",txtUsername.getText())
                .append("password", txtPassword.getText())
                .append("firstname", txtNickname.getText()));
                
        
        reg.insertMany(seedData);
        JLabel label = new JLabel("สมัครสมาชิกสำเร็จ");
        label.setFont(new Font("TH Sarabun New", Font.PLAIN, 15));
        JOptionPane.showMessageDialog(this, label);
        Login log = new Login();
        log.setVisible(true);
        log.pack();
        log.setLocationRelativeTo(null);
        log.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        this.dispose();
    }
    
